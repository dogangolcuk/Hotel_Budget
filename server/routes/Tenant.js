const router = require('express').Router()
const {
  getTenantAll,
  getTenant,
  saveTenant,
  delTenant,
  getTenantForSelect,
  getTenantAllFieldSearch,
  getTenantAllOrdered,
} = require('../controllers/Tenant')

router
  .get('/', async (req, res, next) => {
    try {
      const rows = await getTenantAll()
      res.status(200).send(rows)
    } catch (error) {
      next(error)
    }
  })
  .get('/forselect', async (req, res, next) => {
    try {
      const rows = await getTenantForSelect()
      res.status(200).send(rows)
    } catch (error) {
      next(error)
    }
  })
  .get('/:id', async (req, res, next) => {
    try {
      const { id } = req.params
      const row = await getTenant(id)
      res.status(200).send(row)
    } catch (error) {
      next(error)
    }
  })
  .post('/search', async (req, res, next) => {
    try {
      const rows = await getTenantAllFieldSearch(req.body)
      res.status(200).send(rows)
    } catch (error) {
      next(error)
    }
  })
  .post('/order', async (req, res, next) => {
    try {
      const rows = await getTenantAllOrdered(req.body)
      res.status(200).send(rows)
    } catch (error) {
      next(error)
    }
  })
  .post('/', async (req, res, next) => {
    try {
      const result = await saveTenant(req.body)
      res.status(200).send(result)
    } catch (error) {
      next(error)
    }
  })
  .delete('/:id', async (req, res, next) => {
    try {
      const { id } = req.params
      const result = await delTenant(id)
      res.status(200).send(result)
    } catch (error) {
      next(error)
    }
  })

module.exports = router
